//Using Float Keyword
class floatkey
{
	public static void main(String args[])
	{
		float a = 5.5f;
		float b = 5.5f;
		float c = a + b;
		System.out.println(" We Entered That A Value is " +a);
		System.out.println(" We Entered That B Value is " +b);
		System.out.println(" Addition of Floating Two Values is" +c);
	}
}