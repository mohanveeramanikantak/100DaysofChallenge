# To Find color
color=str(input(" Enter The Color "))
print(" B-Black ")
print(" G-Green ")
print(" Y-Yellow ")
print(" V-Volet ")
print(" W-white ")
print(" Bl=Blue ")
if(color=='B'):
    print("Black")
elif(color=='G'):
    print("Green")
elif(color=='Y'):
    print("Yellow")
elif(color=='V'):
    print("Volet")
elif(color=='W'):
    print("White")
elif(color=='Bl'):
    print("Blue")
else:
    print("Not Mentioned")
    
    
